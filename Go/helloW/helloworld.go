package main

// package main = ponto de entrada do programa
// pacote = conjunto de funções
import "fmt"

// nesse caso o pacote é fmt = algum lugar do código tem fmt
// fmt significa “format” e é o pacote de formatação de entrada e saída do Go.
// Ele fornece funções para: imprimir texto, ler dados do usuário
func main() {
	fmt.Println("Hello, World!")

}

// nome do pacote.nome da função desse pacote

// func = Onde o programa começa a ser executado. Tudo começa por aqui
// dentro da função principal - Print "ln" = pula uma linha

